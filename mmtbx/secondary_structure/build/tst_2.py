def exercise_00(prefix="tst_2_exercise_00"):
  """
  Simple one helix case.
  """
  pdb_inp = iotbx.pdb.input(source_info=None, lines=exercise_00_answer)
  result_h = pdb_inp.construct_hierarchy()
  #result_h.write_pdb_file(file_name="%s_target.pdb"%prefix)
  h_records = """\
HELIX    1   1 ALA E    1  ALA E   16  1                                  16
"""
  ann = ioss.annotation(records=h_records.split('\n'))
  #helices = ioss.parse_helix_records(h_records.split('\n'))
  rm = ssb.substitute_ss(
      real_h=result_h,
      crystal_symmetry=pdb_inp.xray_structure_simple().crystal_symmetry(),
      ss_annotation=ann,
                    )
  selection_cache = result_h.atom_selection_cache()
  isel = selection_cache.iselection("name ca or name n or name o or name c")
  #result_h.write_pdb_file(file_name="%s_substituted1.pdb"%prefix)
  sites_subs1 = result_h.select(isel).atoms().extract_xyz()
  assert rm.geometry.generic_restraints_manager.\
      get_n_ramachandran_proxies() == 0
  assert rm.geometry.generic_restraints_manager.\
      get_n_hbonds() == 12
  assert rm.geometry.generic_restraints_manager.\
      get_n_reference_coordinate_proxies() == 0
  assert rm.geometry.generic_restraints_manager.\
      get_n_reference_torsion_proxies() == 66
  assert rm.geometry.generic_restraints_manager.\
      get_n_den_proxies() == 0
  assert rm.geometry.generic_restraints_manager.\
      get_n_c_beta_dihedral_proxies() == 0
  assert approx_equal(result_h.atoms().extract_xyz(),
      iotbx.pdb.input(source_info=None,lines=ideal_on_target).
      construct_hierarchy().atoms().extract_xyz(),eps=0.2)
  rm = ssb.substitute_ss(
      real_h=result_h,
      crystal_symmetry=pdb_inp.xray_structure_simple().crystal_symmetry(),
      ss_annotation=ann)
  sites_subs2 = result_h.select(isel).atoms().extract_xyz()
  dsub = flex.sqrt((sites_subs2 - sites_subs1).dot())
